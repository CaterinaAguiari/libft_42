#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	unsigned int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		ft_putstr_fd(s, fd);
		write (fd, "\n", 1);
	}
}
